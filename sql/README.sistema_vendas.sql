#  Sistema de Banco de Dados - Vendas

#  Descrição
Projeto de banco de dados simulando um sistema de vendas com clientes, produtos e pedidos.

# Conceitos aplicados
- Modelagem relacional
- PRIMARY KEY e FOREIGN KEY
- INNER JOIN
- GROUP BY
- Funções de agregação

# Consultas
- Total gasto por cliente
- Produto mais vendido
- Faturamento total
- Relatório de pedidos
